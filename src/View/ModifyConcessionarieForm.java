    private void RegistryButtonMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_RegistryButtonMouseClicked
        if("".equals(txtNombreConcesionario.getText()) || "".equals(txtRucConcesionario.getText()) || "".equals(txtCiudadConcesionario.getText())
            || "".equals(txtCiudadConcesionario.getText()) || "".equals(txtTelefonoConcesionario.getText())
            || "".equals(txtCelularConcesionario.getText()) || "".equals(txtEmailConcesionario.getText())
            || "".equals(txtEmailConcesionario.getText()) || "".equals(txtFaxConcesionario.getText())
            || "".equals(txtFaxConcesionario.getText()) || "".equals(txtIdConcesionario.getText())
            || "".equals(txtIdConcesionario.getText()) || "".equals(txtRepresentanteConcesionario.getText())
            || "".equals(txtDireccionConcesionario.getText())
        )
        {
            OkDialog dialog = new OkDialog(this, true);
            dialog.dialogText.setText("All fields are required!");
            dialog.setVisible(true);
        }
        else
        {
            int id;
            String ruc, nombre, direccion, ciudad, representante, telefono, celular, email, fax;
            id=Integer.parseInt(this.txtIdConcesionario.getText());
            ruc=this.txtRucConcesionario.getText();
            nombre=this.txtNombreConcesionario.getText();
            direccion=this.txtDireccionConcesionario.getText();
            ciudad=this.txtCiudadConcesionario.getText();
            representante=this.txtRepresentanteConcesionario.getText();
            telefono=this.txtTelefonoConcesionario.getText();
            celular=this.txtCelularConcesionario.getText();
            email=this.txtEmailConcesionario.getText();
            fax=this.txtFaxConcesionario.getText();
            pieceVO=new ConcessionaireVO(id, ruc,nombre, direccion, ciudad, representante,  telefono, celular, email,fax);
            pieceDAO.modificarConcesionario(pieceVO);
            txtNombreConcesionario.setText("");
            txtRucConcesionario.setText("");
            txtCiudadConcesionario.setText("");
            this.txtCelularConcesionario.setText("");
            txtTelefonoConcesionario.setText("");
            this.txtEmailConcesionario.setText("");
            this.txtFaxConcesionario.setText("");
            this.txtIdConcesionario.setText("");
            this.txtRepresentanteConcesionario.setText("");
            this.txtDireccionConcesionario.setText("");
        }
    }//GEN-LAST:event_RegistryButtonMouseClicked

